/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
// 启动TIM3和TIM4的PWM通道1
HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	uint8_t pwmDirection = 0; // 0: 增加亮度， 1: 减小亮度
uint16_t pwmValue1 = 0;   // 用于TIM3 (板载LED) 的Pulse值
uint16_t pwmValue2 = 0;   // 用于TIM4 (外接LED) 的Pulse值
  while (1)
  {
		// 更新TIM3 (板载LED) 的占空比
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, pwmValue1);
  // 更新TIM4 (外接LED) 的占空比，可以设置一个不同的初始值让效果错开
  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, pwmValue2);

  // 使用HAL_Delay控制变化速度
  HAL_Delay(10);

  // 呼吸灯逻辑：先增后减
  if(pwmDirection == 0)
  {
    // 增加亮度
    pwmValue1 += 5;
    pwmValue2 += 5;
    if(pwmValue1 >= 500) // Period是499，所以最大值是500
    {
      pwmValue1 = 500;
      pwmValue2 = 500;
      pwmDirection = 1; // 切换为减小
    }
  }
  else
  {
    // 减小亮度
    pwmValue1 -= 5;
    pwmValue2 -= 5;
    if(pwmValue1 <= 0)
    {
      pwmValue1 = 0;
      pwmValue2 = 0;
      pwmDirection = 0; // 切换为增加
    }
  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

